#!/usr/bin/env node
/**
 * Build .github/CODEOWNERS from frontmatter `owners:` of all content files.
 *
 * Output format:
 *   - Header comment
 *   - Default catchall: `* @opoudjis` (maintainer covers unmatched files)
 *   - Per-file rules from frontmatter (override the catchall)
 *
 * Per CODEOWNERS precedence, later rules win — per-file overrides go last.
 *
 * Usage: node _meta/build-codeowners.mjs
 */

import { readFile, readdir, writeFile, mkdir } from 'node:fs/promises';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';
import * as YAML from 'yaml';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const CATEGORIES = ['instructions', 'memory', 'settings-fragments', 'project-claude-md', 'rules', 'hooks'];
const DEFAULT_MAINTAINER = 'opoudjis';

async function ownersFor(category, entry) {
  const relPath = `${category}/${entry}`;
  const content = await readFile(join(ROOT, relPath), 'utf-8');
  const match = content.match(/^---\n([\s\S]*?)\n---/);
  if (!match) return null;
  const fm = YAML.parse(match[1]);
  const owners = fm.owners || (fm.author?.github ? [fm.author.github] : []);
  return owners.length ? owners : null;
}

const lines = [
  '# Auto-generated by _meta/build-codeowners.mjs. Do not edit by hand.',
  '# Regenerate after any change to frontmatter `owners:` of a content file.',
  '',
  '# Default catchall: maintainer covers any file not specifically matched below.',
  `* @${DEFAULT_MAINTAINER}`,
  '',
  '# Per-file owners (from frontmatter; later rules override the catchall above)',
];

let perFileCount = 0;

for (const category of CATEGORIES) {
  const dirPath = join(ROOT, category);
  let entries;
  try {
    entries = await readdir(dirPath);
  } catch {
    continue;
  }
  for (const entry of entries.sort()) {
    if (entry === '.gitkeep' || !entry.endsWith('.md')) continue;
    const owners = await ownersFor(category, entry);
    if (owners) {
      lines.push(`${category}/${entry} ${owners.map((o) => `@${o}`).join(' ')}`);
      perFileCount++;
    }
  }
}

if (perFileCount === 0) {
  lines.push('# (no content files yet; per-file rules will populate as seeds land)');
}

await mkdir(join(ROOT, '.github'), { recursive: true });
await writeFile(join(ROOT, '.github', 'CODEOWNERS'), lines.join('\n') + '\n', 'utf-8');
console.log(`Wrote .github/CODEOWNERS: ${perFileCount} per-file rule(s) + default catchall`);
